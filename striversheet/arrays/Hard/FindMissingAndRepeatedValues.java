/**
 * 2965. Find Missing and Repeated Values
 * https://leetcode.com/problems/find-missing-and-repeated-values
 */
class Solution {
	/*
	 * TC: O(n²)
	 * SC: O(1)
	 * The approach uses arithmetic progression & basic algebra for finding missing & repeated values
	 */
	public int[] findMissingAndRepeatedValues(int[][] grid) {
		long n = grid.length * grid.length;				// n²

		long actualSum = 0, actualSqSum = 0;
		for (int i = 0; i < grid.length; i++) {
			for (int j = 0; j < grid.length; j++) {
				actualSum += grid[i][j];				// calculates sum of all elements of grid
				actualSqSum += grid[i][j] * grid[i][j];	// calculates sum of squares of all elements
			}
		}
		long supposedSum = n * (n + 1)/2;				// sum of 1st n natural numbers
		long supposedSqSum = n * (n + 1) * (2*n + 1)/6; // sum of squares of 1st n natural numbers

		long val1 = actualSum - supposedSum;			// x - y	; where x = repeated value
		long val2 = actualSqSum - supposedSqSum;		// x² - y²			y = missing value

		val2 = val2 / val1;								// x + y = (x² - y²)/(x - y)

		long x = val1 + (val2 - val1)/2;				// we know values of (x + y) & (x - y)
		long y = x - val1;								// using algebra, we can find values of "x" & "y"
		
		return new int[]{(int)x, (int)y};
	}
}

